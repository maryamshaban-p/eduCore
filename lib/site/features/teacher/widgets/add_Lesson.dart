            padding: EdgeInsets.zero,
            constraints: const BoxConstraints(),
          ),
        ],
      ),
    );
  }

  /// Single-file picker — still used for Homework (the API only accepts
  /// one HomeworkFile).
  Widget _filePicker({
    required PlatformFile? file,
    required VoidCallback onPick,
    required VoidCallback onClear,
  }) {
    return Row(
      children: [
        OutlinedButton.icon(
          onPressed: onPick,
          icon: const Icon(Icons.upload_rounded, size: 16),
          label: const Text('Choose File'),
          style: OutlinedButton.styleFrom(
            foregroundColor: AppColors.slate700,
            side: const BorderSide(color: AppColors.slate300),
            textStyle: const TextStyle(fontFamily: 'Inter', fontSize: 13),
            padding: const EdgeInsets.symmetric(horizontal: 14, vertical: 10),
            shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(6)),
          ),
        ),
        const SizedBox(width: 10),
        Expanded(
          child: file == null
              ? const Text(
                  'No file chosen',
                  style: TextStyle(
                    fontFamily: 'Inter', fontSize: 13, color: AppColors.slate400,
                  ),
                )
              : Row(
                  children: [
                    Expanded(
                      child: Text(
                        file.name,
                        overflow: TextOverflow.ellipsis,
                        style: const TextStyle(
                          fontFamily: 'Inter', fontSize: 13, color: AppColors.slate700,
                        ),
                      ),
                    ),
                    GestureDetector(
                      onTap: onClear,
                      child: const Icon(Icons.close_rounded,
                          size: 16, color: AppColors.slate400),
                    ),
                  ],
                ),
        ),
      ],
    );
  }

  /// Multi-file picker — used for the `Files` attachments field. Shows a
  /// "Choose Files" button plus a chip-style list of everything picked so
  /// far, each individually removable, instead of only ever holding one
  /// file at a time.
  Widget _multiFilePicker({
    required List<PlatformFile> files,
    required VoidCallback onPick,
    required void Function(int) onRemoveAt,
  }) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Row(
          children: [
            OutlinedButton.icon(
              onPressed: onPick,
              icon: const Icon(Icons.upload_rounded, size: 16),
              label: const Text('Choose Files'),
              style: OutlinedButton.styleFrom(
                foregroundColor: AppColors.slate700,
                side: const BorderSide(color: AppColors.slate300),
                textStyle: const TextStyle(fontFamily: 'Inter', fontSize: 13),
                padding: const EdgeInsets.symmetric(horizontal: 14, vertical: 10),
                shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(6)),
              ),
            ),
            const SizedBox(width: 10),
            if (files.isEmpty)
              const Expanded(
                child: Text(
                  'No files chosen',
                  style: TextStyle(
                    fontFamily: 'Inter', fontSize: 13, color: AppColors.slate400,
                  ),
                ),
              )
            else
              Text(
                '${files.length} file${files.length == 1 ? '' : 's'} selected',
                style: const TextStyle(
                  fontFamily: 'Inter', fontSize: 12, color: AppColors.slate500,
                ),
              ),
          ],
        ),
        if (files.isNotEmpty) ...[
          const SizedBox(height: 10),
          Column(
            children: List.generate(files.length, (i) {
              final file = files[i];
              return Container(
                margin: const EdgeInsets.only(bottom: 6),
                padding: const EdgeInsets.symmetric(horizontal: 10, vertical: 8),
                decoration: BoxDecoration(
                  color: AppColors.slate50,
                  borderRadius: BorderRadius.circular(6),
                  border: Border.all(color: AppColors.slate200),
                ),
                child: Row(
                  children: [
                    const Icon(Icons.insert_drive_file_outlined,
                        size: 16, color: AppColors.slate400),
                    const SizedBox(width: 8),
                    Expanded(
                      child: Text(
                        file.name,
                        overflow: TextOverflow.ellipsis,
                        style: const TextStyle(
                          fontFamily: 'Inter', fontSize: 13, color: AppColors.slate700,
                        ),
                      ),
                    ),
                    GestureDetector(
                      onTap: () => onRemoveAt(i),
                      child: const Padding(
                        padding: EdgeInsets.all(2),
                        child: Icon(Icons.close_rounded,
                            size: 16, color: AppColors.slate400),
                      ),
                    ),
                  ],
                ),
              );
            }),
          ),
        ],
      ],
    );
  }

  Widget _entryTestToggle() {
    return GestureDetector(
      onTap: () => setState(() => _hasEntryTest = !_hasEntryTest),
      child: Row(children: [
        AnimatedContainer(
          duration: const Duration(milliseconds: 150),
          width: 20,
          height: 20,
          decoration: BoxDecoration(
            borderRadius: BorderRadius.circular(4),
            color: _hasEntryTest ? AppColors.primary : Colors.white,
            border: Border.all(
              color: _hasEntryTest ? AppColors.primary : AppColors.slate300,
              width: 1.5,
            ),
          ),
          child: _hasEntryTest
              ? const Icon(Icons.check, size: 14, color: Colors.white)
              : null,
        ),
        const SizedBox(width: 10),
        const Text(
          'Enable Entry Test',
          style: TextStyle(
            fontFamily: 'Inter', fontSize: 13, color: AppColors.slate700,
          ),
        ),
      ]),
    );
  }

  Widget _buildEntryTestSection() {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        const SizedBox(height: 16),

        // ── Manual / AI tab switcher ──────────────────────────────────────
        Container(
          decoration: BoxDecoration(
            color: AppColors.slate100,
            borderRadius: BorderRadius.circular(8),
          ),
          padding: const EdgeInsets.all(4),
          child: Row(children: [
            _modeTab(label: '✏️  Create Manually', selected: !_isAiMode,
                onTap: () => setState(() => _isAiMode = false)),
            _modeTab(label: '✨  Create with AI', selected: _isAiMode,
                onTap: () => setState(() => _isAiMode = true)),
          ]),
        ),
        const SizedBox(height: 16),

        // ── Mode body ─────────────────────────────────────────────────────
        _isAiMode ? _buildAiMode() : _buildManualMode(),

        // ── Passing score & retake interval (shared) ──────────────────────
        const SizedBox(height: 16),
        Row(children: [
          Expanded(child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              _label('Passing Score %'),
              const SizedBox(height: 6),
              custom_textField(controller: _passingScoreController),
            ],
          )),
          const SizedBox(width: 12),
          Expanded(child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              _label('Retake Interval (hrs)'),
              const SizedBox(height: 6),
              custom_textField(controller: _retakeIntervalController),
            ],
          )),
        ]),
      ],
    );
  }

  Widget _modeTab({
    required String label,
    required bool selected,
    required VoidCallback onTap,
  }) {
    return Expanded(
      child: GestureDetector(
        onTap: onTap,
        child: AnimatedContainer(
          duration: const Duration(milliseconds: 150),
          padding: const EdgeInsets.symmetric(vertical: 8),
          decoration: BoxDecoration(
            color: selected ? Colors.white : Colors.transparent,
            borderRadius: BorderRadius.circular(6),
            boxShadow: selected
                ? [BoxShadow(color: Colors.black.withOpacity(0.08),
                    blurRadius: 4, offset: const Offset(0, 1))]
                : [],
          ),
          child: Text(
            label,
            textAlign: TextAlign.center,
            style: TextStyle(
              fontFamily: 'Inter',
              fontSize: 13,
              fontWeight: selected ? FontWeight.w600 : FontWeight.w400,
              color: selected ? AppColors.primary : AppColors.slate500,
            ),
          ),
        ),
      ),
    );
  }

  // ── Manual Mode ────────────────────────────────────────────────────────────
  Widget _buildManualMode() {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Row(
          mainAxisAlignment: MainAxisAlignment.spaceBetween,
          children: [
            _label('Test Questions'),
            TextButton.icon(
              onPressed: _addQuestion,
              icon: const Icon(Icons.add_rounded, size: 16, color: AppColors.primary),
              label: const Text(
                'Add Question',
                style: TextStyle(
                  fontFamily: 'Inter', fontSize: 13, color: AppColors.primary,
                ),
              ),
              style: TextButton.styleFrom(padding: EdgeInsets.zero),
            ),
          ],
        ),
        const SizedBox(height: 8),
        ..._questions.asMap().entries.map((e) => _buildQuestionCard(e.key, e.value)),
      ],
    );
  }

  Widget _buildQuestionCard(int index, _QuestionDraft q) {
    return Container(
      margin: const EdgeInsets.only(bottom: 16),
      padding: const EdgeInsets.all(14),
      decoration: BoxDecoration(
        border: Border.all(color: AppColors.slate200),
        borderRadius: BorderRadius.circular(8),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Question header
          Row(
            children: [
              Text(
                'Question ${index + 1}',
                style: const TextStyle(
                  fontFamily: 'Inter', fontSize: 13,
                  fontWeight: FontWeight.w600, color: AppColors.slate700,
                ),
              ),
              const Spacer(),
              // Question Type dropdown (only Multiple Choice for now)
              const Text(
                'Multiple Choice',
                style: TextStyle(
                  fontFamily: 'Inter', fontSize: 12, color: AppColors.slate500,
                ),
              ),
              if (_questions.length > 1) ...[
                const SizedBox(width: 8),
                GestureDetector(
                  onTap: () => _removeQuestion(index),
                  child: const Icon(Icons.delete_outline_rounded,
                      size: 18, color: AppColors.slate400),
                ),
              ],
            ],
          ),
          const SizedBox(height: 10),

          // Question text
          const Text(
            'Question Text *',
            style: TextStyle(
              fontFamily: 'Inter', fontSize: 12,
              fontWeight: FontWeight.w500, color: AppColors.slate600,
            ),
          ),
          const SizedBox(height: 5),
          custom_textField(
            controller: q.text,
            hintText: 'Enter question...',
          ),
          const SizedBox(height: 12),

          // Answer options
          const Text(
            'Answer Options',
            style: TextStyle(
              fontFamily: 'Inter', fontSize: 12,
              fontWeight: FontWeight.w500, color: AppColors.slate600,
            ),
          ),
          const SizedBox(height: 6),
          ...List.generate(q.options.length, (i) => _buildOptionRow(q, i)),
          const SizedBox(height: 4),
          const Text(
            'Select the radio button next to the correct answer.',
            style: TextStyle(
              fontFamily: 'Inter', fontSize: 11, color: AppColors.slate400,
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildOptionRow(_QuestionDraft q, int i) {
    return Padding(
      padding: const EdgeInsets.only(bottom: 8),
      child: Row(
        children: [
          GestureDetector(
            onTap: () => setState(() => q.correctIndex = i),
            child: Container(
              width: 18, height: 18,
              decoration: BoxDecoration(
                shape: BoxShape.circle,
                border: Border.all(
                  color: q.correctIndex == i
                      ? AppColors.primary : AppColors.slate300,
                  width: 2,
                ),
              ),
              child: q.correctIndex == i
                  ? Center(
                      child: Container(
                        width: 8, height: 8,
                        decoration: const BoxDecoration(
                          shape: BoxShape.circle,
                          color: AppColors.primary,
                        ),
                      ),
                    )
                  : null,
            ),
          ),
          const SizedBox(width: 8),
          Expanded(
            child: custom_textField(
              controller: q.options[i].text,
              hintText: 'Option ${i + 1}',
            ),
          ),
        ],
      ),
    );
  }

  // ── AI Mode ────────────────────────────────────────────────────────────────
  Widget _buildAiMode() {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        // Upload lesson content
        _label('Upload Lesson Content *'),
        const SizedBox(height: 8),
        _filePicker(
          file: _aiContentFile,
          onPick: _pickAiContentFile,
          onClear: () => setState(() {
            _aiContentFile = null;
            _aiGeneratedQuestions = null;
          }),
        ),
        const SizedBox(height: 4),
        const Text(
          'PDF only — AI will analyze the content to generate questions.',
          style: TextStyle(
            fontFamily: 'Inter', fontSize: 11, color: AppColors.slate400,
          ),
        ),
        const SizedBox(height: 16),

        // Question types
        // "Short Answer" removed — no AI endpoint backs it, only
        // Multiple Choice (/ai/quiz) and True/False (/ai/true-false) exist.
        _label('Question Types *'),
        const SizedBox(height: 8),
        Wrap(
          spacing: 8,
          children: ['Multiple Choice', 'True / False']
              .map((type) {
            final selected = _selectedQTypes.contains(type);
            return GestureDetector(
              onTap: () => setState(() {
                if (selected && _selectedQTypes.length > 1) {
                  _selectedQTypes.remove(type);
                } else {
                  _selectedQTypes.add(type);
                }
                // Question-type changes invalidate any previous generation.
                _aiGeneratedQuestions = null;
              }),
              child: AnimatedContainer(
                duration: const Duration(milliseconds: 150),
                padding: const EdgeInsets.symmetric(horizontal: 14, vertical: 8),
                decoration: BoxDecoration(
                  color: selected ? AppColors.primary : Colors.white,
                  border: Border.all(
                    color: selected ? AppColors.primary : AppColors.slate300,
                  ),
                  borderRadius: BorderRadius.circular(20),
                ),
                child: Text(
                  type,
                  style: TextStyle(
                    fontFamily: 'Inter',
                    fontSize: 13,
                    color: selected ? Colors.white : AppColors.slate600,
                  ),
                ),
              ),
            );
          }).toList(),
        ),
        const SizedBox(height: 16),

        // Number of questions
        _label('Number of Questions'),
        const SizedBox(height: 6),
        custom_textField(controller: _numQuestionsController),
        const SizedBox(height: 16),

        // Generate button
        SizedBox(
          width: double.infinity,
          child: ElevatedButton.icon(
            style: ElevatedButton.styleFrom(
              backgroundColor: AppColors.primary,
              padding: const EdgeInsets.symmetric(vertical: 13),
              shape: RoundedRectangleBorder(
                  borderRadius: BorderRadius.circular(8)),
            ),
            onPressed: (_aiContentFile == null || _isGeneratingAi)
                ? null
                : _generateAiQuestions,
            icon: _isGeneratingAi
                ? const SizedBox(
                    width: 16, height: 16,
                    child: CircularProgressIndicator(
                        color: Colors.white, strokeWidth: 2),
                  )
                : const Icon(Icons.auto_awesome_rounded,
                    size: 16, color: Colors.white),
            label: Text(
              _isGeneratingAi ? 'Generating...' : 'Generate Test with AI',
              style: const TextStyle(
                fontFamily: 'Inter',
                fontSize: 14,
                fontWeight: FontWeight.w600,
                color: Colors.white,
              ),
            ),
          ),
        ),

        // ── Generated-questions preview ─────────────────────────────────
        if (_aiGeneratedQuestions != null) ...[
          const SizedBox(height: 16),
          _buildAiPreview(),
        ],
      ],
    );
  }

  Widget _buildAiPreview() {
    final questions = _aiGeneratedQuestions!;
    if (questions.isEmpty) {
      return const SizedBox.shrink();
    }

    final visibleCount =
        _aiPreviewExpanded ? questions.length : (questions.length > 5 ? 5 : questions.length);
    final hiddenCount = questions.length - visibleCount;

    return Container(
      width: double.infinity,
      padding: const EdgeInsets.all(14),
      decoration: BoxDecoration(
        color: AppColors.successBg,
        borderRadius: BorderRadius.circular(8),
        border: Border.all(color: AppColors.success.withOpacity(0.3)),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(children: [
            const Icon(Icons.check_circle_outline_rounded, color: AppColors.success, size: 18),
            const SizedBox(width: 8),
            Expanded(
              child: Text(
                'Generated ${questions.length} question${questions.length == 1 ? '' : 's'} — they will be added to this lesson\'s entry test.',
                style: const TextStyle(
                  fontFamily: 'Inter', fontSize: 12, color: AppColors.success,
                ),
              ),
            ),
          ]),
          const SizedBox(height: 12),
          ...questions.take(visibleCount).toList().asMap().entries.map((entry) {
            final i = entry.key;
            final q = entry.value;
            return Padding(
              padding: const EdgeInsets.only(bottom: 12),
              child: _AiQuestionPreviewCard(index: i, question: q),
            );
          }),
          if (questions.length > 5)
            GestureDetector(
              onTap: () => setState(() => _aiPreviewExpanded = !_aiPreviewExpanded),
              child: Padding(
                padding: const EdgeInsets.only(top: 2, bottom: 4),
                child: Row(
                  mainAxisSize: MainAxisSize.min,
                  children: [
                    Text(
                      _aiPreviewExpanded ? 'Show less' : '+ $hiddenCount more',
                      style: const TextStyle(
                        fontFamily: 'Inter',
                        fontSize: 12,
                        fontWeight: FontWeight.w600,
                        color: AppColors.success,
                      ),
                    ),
                    const SizedBox(width: 4),
                    Icon(
                      _aiPreviewExpanded
                          ? Icons.keyboard_arrow_up_rounded
                          : Icons.keyboard_arrow_down_rounded,
                      size: 16,
                      color: AppColors.success,
                    ),
                  ],
                ),
              ),
            ),
        ],
      ),
    );
  }

  // ── Helpers ────────────────────────────────────────────────────────────────
  Widget _label(String text) => Text(
        text,
        style: const TextStyle(
          fontFamily: 'Inter',
          fontSize: 13,
          fontWeight: FontWeight.w500,
          color: AppColors.slate700,
        ),
      );
}

/// Shows one generated question's text plus its full options, with the
/// correct option highlighted — previously the preview only showed the
/// question text, with no way to check the options or which one the AI
/// marked as correct before saving the lesson.
class _AiQuestionPreviewCard extends StatelessWidget {
  final int index;
  final Map<String, dynamic> question;

  const _AiQuestionPreviewCard({required this.index, required this.question});

  @override
  Widget build(BuildContext context) {
    final text = (question['text'] ?? '').toString();
    final options = (question['options'] as List? ?? [])
        .map((o) => Map<String, dynamic>.from(o as Map))
        .toList();

    return Container(
      padding: const EdgeInsets.all(10),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(8),
        border: Border.all(color: AppColors.success.withOpacity(0.25)),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            '${index + 1}. $text',
            style: const TextStyle(
              fontFamily: 'Inter', fontSize: 12, fontWeight: FontWeight.w600,
              color: AppColors.slate800,
            ),
          ),
          if (options.isNotEmpty) ...[
            const SizedBox(height: 6),
            ...options.map((o) {
              final optText = (o['text'] ?? '').toString();
              final isCorrect = o['isCorrect'] == true;
              return Padding(
                padding: const EdgeInsets.only(top: 3),
                child: Row(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Icon(
                      isCorrect
                          ? Icons.check_circle_rounded
                          : Icons.circle_outlined,
                      size: 14,
                      color: isCorrect ? AppColors.success : AppColors.slate400,
                    ),
                    const SizedBox(width: 6),
                    Expanded(
                      child: Text(
                        optText,
                        style: TextStyle(
                          fontFamily: 'Inter',
                          fontSize: 12,
                          color: isCorrect ? AppColors.success : AppColors.slate600,
                          fontWeight: isCorrect ? FontWeight.w600 : FontWeight.w400,
                        ),
                      ),
                    ),
                  ],
                ),
              );
            }),
          ],
        ],
      ),
    );
  }
}