                    _label('Lesson Title *'),
                    const SizedBox(height: 6),
                    custom_textField(
                      controller: _titleController,
                      hintText: 'Enter lesson title',
                    ),
                    const SizedBox(height: 20),

                    // ── Attachment Upload (multiple files) ──────────────────
