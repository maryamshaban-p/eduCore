// ─── Entry-test question model ───────────────────────────────────────────────
class _QuestionDraft {
  final TextEditingController text = TextEditingController();
  final List<_OptionDraft> options = [
    _OptionDraft(), _OptionDraft(), _OptionDraft(), _OptionDraft(),
  ];
  int correctIndex = 0;

  void dispose() {
    text.dispose();
    for (final o in options) o.dispose();
  }
}

class _OptionDraft {
  final TextEditingController text = TextEditingController();
  void dispose() => text.dispose();
}

// ─── Widget ──────────────────────────────────────────────────────────────────
class AddLesson extends StatefulWidget {
  final bool isVisible;
  final VoidCallback onClose;
  final int courseId;

  const AddLesson({
    super.key,
    required this.isVisible,
    required this.onClose,
    required this.courseId,
  });

  @override
  State<AddLesson> createState() => _AddLessonState();
}

class _AddLessonState extends State<AddLesson> {
  final TeacherSubjectService _service = TeacherSubjectService();

  // ── Basic fields ──────────────────────────────────────────────────────────
  final _titleController = TextEditingController();
  final _availabilityController = TextEditingController(text: '14');
  final _maxViewsController = TextEditingController(text: '5');

  // The API's `Files` field is an array, so a lesson can have multiple
  // general attachments — not just one.
  final List<PlatformFile> _attachmentFiles = [];

  // Homework is now a file upload (HomeworkFile is binary on the API),
  // not a text link — so it's a PlatformFile instead of a controller.
  // Unlike Files, the API only accepts a single HomeworkFile.
  PlatformFile? _homeworkFile;

  // ── Entry test ────────────────────────────────────────────────────────────
  bool _hasEntryTest = false;
  bool _isAiMode = false; // false = Manual, true = AI

  // Manual mode
  final List<_QuestionDraft> _questions = [];

  // AI mode
  PlatformFile? _aiContentFile;
  final _numQuestionsController = TextEditingController(text: '5');
  final _passingScoreController = TextEditingController(text: '60');
  final _retakeIntervalController = TextEditingController(text: '24');
  // "Short Answer" removed — there's no backing AI endpoint for it
  // (only /ai/quiz for Multiple Choice and /ai/true-false for True/False
  // exist), so it was never actually generating anything; offering it as
  // a selectable type was misleading.
  Set<String> _selectedQTypes = {'Multiple Choice'};

  // Questions generated by the AI endpoints, normalized to the same shape
  // `addEntryTest` expects: { text, options: [{ text, isCorrect }] }.
  // Cleared whenever the source file / question types / count change so a
  // stale generation can never be silently submitted.
  List<Map<String, dynamic>>? _aiGeneratedQuestions;
  bool _isGeneratingAi = false;
  // Whether the "+ N more" toggle in the AI preview is expanded to show
  // every generated question instead of just the first 5.
  bool _aiPreviewExpanded = false;

  bool _isSaving = false;

  // ─────────────────────────────────────────────────────────────────────────
  @override
  void initState() {
    super.initState();
    _addQuestion(); // start with one blank question in manual mode
    // A changed question count invalidates any previous AI generation —
    // tracked via listener instead of relying on custom_textField exposing
    // onChanged.
    _numQuestionsController.addListener(_invalidateAiGeneration);
  }

  void _invalidateAiGeneration() {
    if (_aiGeneratedQuestions != null) {
      setState(() => _aiGeneratedQuestions = null);
    }
  }

  @override
  void dispose() {
    _titleController.dispose();
    _availabilityController.dispose();
    _maxViewsController.dispose();
    _numQuestionsController.removeListener(_invalidateAiGeneration);
    _numQuestionsController.dispose();
    _passingScoreController.dispose();
    _retakeIntervalController.dispose();
    for (final q in _questions) q.dispose();
    super.dispose();
  }

  void _addQuestion() {
    setState(() => _questions.add(_QuestionDraft()));
  }

  void _removeQuestion(int i) {
    setState(() {
      _questions[i].dispose();
      _questions.removeAt(i);
    });
  }

  // `withData: true` is required so PlatformFile.bytes is populated.
  // On Flutter Web there is no filesystem, so PlatformFile.path is always
  // null and dart:io-based APIs (like Dio's MultipartFile.fromFile) throw
  // "Unsupported operation" if bytes aren't available as a fallback.
  //
  // `allowMultiple: true` lets the teacher attach more than one file at
  // once, matching the API's `Files` field which is an array. Picking
  // again adds to the existing selection rather than replacing it, so
  // multiple picker trips can build up the same attachment list.
  Future<void> _pickAttachmentFiles() async {
    final result = await FilePicker.pickFiles(
      withData: true,
      allowMultiple: true,
    );
    if (result != null && result.files.isNotEmpty) {
      setState(() => _attachmentFiles.addAll(result.files));
    }
  }

  void _removeAttachmentFile(int index) {
    setState(() => _attachmentFiles.removeAt(index));
  }

  // Picks the homework file. `withData: true` so PlatformFile.bytes is
  // populated — needed on Flutter Web where `.path` isn't available, and
  // harmless to keep on mobile/desktop too since homework files are
  // typically small documents (not large videos).
  Future<void> _pickHomeworkFile() async {
    final result = await FilePicker.pickFiles(
      type: FileType.custom,
      allowedExtensions: ['pdf', 'doc', 'docx', 'txt', 'pptx', 'zip'],
      withData: true,
    );
    if (result != null && result.files.isNotEmpty) {
      setState(() => _homeworkFile = result.files.first);
    }
  }

  Future<void> _pickAiContentFile() async {
    final result = await FilePicker.pickFiles(
      type: FileType.custom,
      allowedExtensions: ['pdf'],
      withData: true,
    );
    if (result != null && result.files.isNotEmpty) {
      setState(() {
        _aiContentFile = result.files.first;
        // A new source file invalidates whatever was generated before.
        _aiGeneratedQuestions = null;
      });
    }
  }

  /// Calls the AI generation endpoints (`/ai/quiz` for Multiple Choice and
  /// `/ai/true-false` for True/False — both take the uploaded PDF plus
  /// `numQuestions`) for every selected question type, and stores the
  /// combined, normalized result in [_aiGeneratedQuestions].
  Future<void> _generateAiQuestions() async {
    if (_aiContentFile == null) return;

    setState(() {
      _isGeneratingAi = true;
      _aiGeneratedQuestions = null;
    });

    final numQuestions = int.tryParse(_numQuestionsController.text) ?? 5;
    final results = <Map<String, dynamic>>[];

    try {
      if (_selectedQTypes.contains('Multiple Choice')) {
        final mcq = await _service.generateAiQuestions(
          pdf: _aiContentFile!,
          numQuestions: numQuestions,
          trueFalse: false,
        );
        results.addAll(mcq);
      }

      if (_selectedQTypes.contains('True / False')) {
        final tf = await _service.generateAiQuestions(
          pdf: _aiContentFile!,
          numQuestions: numQuestions,
          trueFalse: true,
        );
        results.addAll(tf);
      }

      if (!mounted) return;

      setState(() {
        _aiGeneratedQuestions = results;
        _aiPreviewExpanded = false;
      });

      if (results.isEmpty) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text(
            'Select at least Multiple Choice or True/False to generate questions.',
          )),
        );
      }
    } catch (e) {
      if (!mounted) return;
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Failed to generate questions: $e')),
      );
    } finally {
      if (mounted) setState(() => _isGeneratingAi = false);
    }
  }

  Future<void> _saveLesson() async {
    if (_titleController.text.isEmpty) return;
    setState(() => _isSaving = true);
    try {
      // Build entry-test payload if needed — either from the manually
      // typed questions, or from whatever the AI step generated.
      List<Map<String, dynamic>>? questions;
      if (_hasEntryTest && !_isAiMode && _questions.isNotEmpty) {
        questions = _questions.map((q) {
          return {
            'text': q.text.text.trim(),
            'options': List.generate(q.options.length, (i) => {
              'text': q.options[i].text.text.trim(),
              'isCorrect': i == q.correctIndex,
            }),
          };
        }).toList();
      } else if (_hasEntryTest && _isAiMode && _aiGeneratedQuestions != null) {
        questions = _aiGeneratedQuestions;
      }

      final lessonId = await _service.addLesson(
        courseId: widget.courseId,
        title: _titleController.text.trim(),
        attachmentFiles: _attachmentFiles,
        availableDays: int.tryParse(_availabilityController.text) ?? 14,
        maxViews: int.tryParse(_maxViewsController.text) ?? 5,
        homeworkFile: _homeworkFile,
        hasEntryTest: _hasEntryTest,
      );

      if (_hasEntryTest && questions != null && questions.isNotEmpty) {
        await _service.addEntryTest(
          lessonId: lessonId,
          title: _titleController.text.trim(),
          passingScore: int.tryParse(_passingScoreController.text) ?? 60,
          retakeIntervalHours: int.tryParse(_retakeIntervalController.text) ?? 24,
          questions: questions,
        );
      }

      _titleController.clear();
      setState(() {
        _attachmentFiles.clear();
        _homeworkFile = null;
        _hasEntryTest = false;
        _aiContentFile = null;
        _aiGeneratedQuestions = null;
      });
      widget.onClose();
    } catch (e) {
      if (!mounted) return;
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Failed to save lesson: $e')),
      );
    } finally {
      if (mounted) setState(() => _isSaving = false);
    }
  }

