                      style: TextStyle(
                        fontFamily: 'Inter', fontSize: 11, color: AppColors.slate400,
                      ),
                    ),
                    const SizedBox(height: 20),

                    // ── Availability & Max Views ────────────────────────────
                    Row(children: [
                      Expanded(child: Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          _label('Availability (days)'),
                          const SizedBox(height: 6),
                          custom_textField(controller: _availabilityController),
                        ],
                      )),
                      const SizedBox(width: 12),
                      Expanded(child: Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          _label('Max Views'),
                          const SizedBox(height: 6),
                          custom_textField(controller: _maxViewsController),
                        ],
                      )),
                    ]),
                    const SizedBox(height: 20),

                    // ── Homework File ───────────────────────────────────────
                    _label('Homework File'),
                    const SizedBox(height: 8),
                    _filePicker(
                      file: _homeworkFile,
                      onPick: _pickHomeworkFile,
                      onClear: () => setState(() => _homeworkFile = null),
                    ),
                    const SizedBox(height: 4),
                    const Text(
                      'Upload the homework document (PDF, DOC, DOCX, TXT, PPTX, ZIP)',
                      style: TextStyle(
                        fontFamily: 'Inter', fontSize: 11, color: AppColors.slate400,
                      ),
                    ),
                    const SizedBox(height: 20),

                    // ── Enable Entry Test toggle ────────────────────────────
                    _entryTestToggle(),

                    // ── Entry Test expanded section ─────────────────────────
                    AnimatedSize(
                      duration: const Duration(milliseconds: 250),
                      curve: Curves.easeInOut,
                      child: _hasEntryTest
                          ? _buildEntryTestSection()
                          : const SizedBox.shrink(),
                    ),

                    const SizedBox(height: 28),

                    // ── Save button ─────────────────────────────────────────
                    SizedBox(
                      width: double.infinity,
                      child: ElevatedButton(
                        style: ElevatedButton.styleFrom(
                          backgroundColor: AppColors.primary,
                          padding: const EdgeInsets.symmetric(vertical: 14),
                          shape: RoundedRectangleBorder(
                              borderRadius: BorderRadius.circular(8)),
                        ),
                        onPressed: _isSaving ? null : _saveLesson,
                        child: _isSaving
                            ? const SizedBox(
                                width: 18, height: 18,
                                child: CircularProgressIndicator(
                                    color: Colors.white, strokeWidth: 2))
                            : const Text(
                                'Save Lesson',
                                style: TextStyle(
                                  fontFamily: 'Inter',
                                  fontSize: 14,
                                  fontWeight: FontWeight.w600,
                                  color: Colors.white,
                                ),
                              ),
                      ),
                    ),
                  ],
                ),
              ),
            ),
          ],
        ),
      ),
    );
  }

